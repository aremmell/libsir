function Echo-Blue($msg)
{
    write-host -ForegroundColor Blue $msg
}

function Echo-Red($msg)
{
    write-host -ForegroundColor Red $msg
}

function Echo-Green($msg)
{
    write-host -ForegroundColor Green $msg
}
